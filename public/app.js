"use strict";
// classes
class Invoice {
    constructor(c, d, a) {
        this.client = c;
        this.details = d;
        this.amount = a;
    }
    format() {
        return `${this.client} owes $${this.amount} for ${this.details}`;
    }
}
const invoiceOne = new Invoice('mario', 'website', 250);
const invoiceTwo = new Invoice('luigi', 'app', 900);
console.log(invoiceOne, invoiceTwo);
